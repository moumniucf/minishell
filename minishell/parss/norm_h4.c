#include "../minishell.h"

int	dollar_count2(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] && s[i] != '"')
	{
		if (s[i] == '$')
		{
			j++;
		}
		i++;
	}
	return (j);
}
